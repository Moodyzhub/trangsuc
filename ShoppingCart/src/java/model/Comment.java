package model;

/**
 *
 * @author admin
 */
public class Comment {
    int pid;
    int uid;
    String content;
    String name;

    public Comment(int pid, int uid, String content, String name) {
        this.pid = pid;
        this.uid = uid;
        this.content = content;
        this.name = name;
    }

    public int getPid() {
        return pid;
    }

    public void setPid(int pid) {
        this.pid = pid;
    }

    public int getUid() {
        return uid;
    }

    public void setUid(int uid) {
        this.uid = uid;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
